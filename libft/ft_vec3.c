#include "libft.h"

t_vec	ft_vectoradd(t_vec v1, t_vec v2)
{
	t_vec	sub;

	sub.x = v1.x + v2.x;
	sub.y = v1.y + v2.y;
	sub.z = v1.z + v2.z;
	return (sub);
}
