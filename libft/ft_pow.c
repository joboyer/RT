#include "libft.h"

double	ft_pow(double i, int p)
{
	double r;

	r = i;
	if (p == 1)
		return (i);
	if (p < 0)
		return (0);
	while (p)
	{
		r = r * i;
		p--;
	}
	return (r);
}
